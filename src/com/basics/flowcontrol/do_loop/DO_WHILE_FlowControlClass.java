package com.basics.flowcontrol.do_loop;

public class DO_WHILE_FlowControlClass {

	public static void main(String[] args) {
		
		 //do ...while -Statement
	      int count = 0;
	      do{
	           System.out.print(count);
	           count ++;
	       }
	       while (count < 11);
    }	

}
